#include<stdio.h>
#include<stdlib.h>
int main()
{
	int i,j,t,x,k,sum,prev,a[100];
	scanf("%d",&t);
	for(j=0;j<t;j++)
	{
		sum=0;
		scanf("%d%d",&x,&k);
		for(i=0;i<x;i++)
		{
			scanf("%d",&a[i]);
		}
		prev=0;
		for(i=0;i<x;i++)
		{
				if(prev==a[i])
				{
					sum=sum+(a[i]-1);
					prev=(a[i]-1);
				}
				else
				{
					sum=sum+a[i];
					prev=a[i];
				}
		}
		printf("%d",sum);
	}
}